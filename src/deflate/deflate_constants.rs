pub fn quantize_distance(dist: u32) -> usize {
    DIST_CODE_TABLE[if dist <= 256 {
        dist - 1
    } else {
        256 + ((dist - 1) >> 7)
    } as usize]
        .into()
}
pub fn quantize_length(len: u32) -> usize {
    LENGTH_CODE_TABLE[len as usize - MIN_MATCH as usize].into()
}
